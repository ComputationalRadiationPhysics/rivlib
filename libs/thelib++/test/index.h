#ifndef THETEST_INDEX_H_INCLUDED
#define THETEST_INDEX_H_INCLUDED
#if (defined(_MSC_VER) && (_MSC_VER > 1000))
#pragma once
#endif /* (defined(_MSC_VER) && (_MSC_VER > 1000)) */


/** type for basic test functions */
typedef void (*TheTestFunctionType)(void);


/** type for test index structure */
typedef struct _TheTestIndexEntry_t {

    /** the tests name. Used as command line argument to select this test. */
    const TCHAR *testName;

    /** the function called when this test is selected. */
    TheTestFunctionType testFunc;

    /** the description of this test. Used for the online help. */
    const TCHAR *testDesc;

    /** Flag whether or not to include this test when testing all */
    bool isPartOfAll;

} TheTestIndexEntry;


extern TheTestIndexEntry TheTestIndex[];

#endif /* THETEST_INDEX_H_INCLUDED */
